import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";
import pool from "../db.js";

export const loginUser = async (req, res) => {
  const { identifier, password, role } = req.body;
  console.log("Login attempt for:", identifier, " Role: ", role);

  if (!identifier || !password || !role) {
    return res.status(400).json({
      success: false,
      message: "Please provide email/mobile and password.",
    });
  }

  try {
    // 1. Find user by either Email OR Phone
    const [users] = await pool.query(
      `SELECT user_id, role, password_hash FROM users WHERE role = ? AND email = ? OR phone = ?`,
      [role, identifier, identifier],
    );

    if (users.length === 0) {
      return res.status(401).json({
        success: false,
        message: "Invalid credentials. User not found.",
      });
    }

    const user = users[0];

    // 2. Verify Password
    // If you haven't implemented bcrypt hashing in your signup yet, and passwords are plain text,
    // replace the bcrypt.compare line with: const isMatch = (password === user.password_hash);
    const isMatch = await bcrypt.compare(password, user.password_hash);

    if (!isMatch) {
      return res
        .status(401)
        .json({ success: false, message: "Invalid password." });
    }

    // 3. Success! Return user info
    res.json({
      success: true,
      userId: user.user_id,
      role: user.role,
      message: "Login successful",
    });
  } catch (error) {
    console.error("Login Error:", error);
    res.status(500).json({ success: false, message: "Internal server error" });
  }
};

export const signup = async (req, res) => {
  const connection = await pool.getConnection();
  try {
    await connection.beginTransaction();

    const data = req.body;
    console.log("Received signup data:", data);

    // Hash password
    const passwordHash = await bcrypt.hash(data.password, 10);

    // 1. INSERT user including DOB - TRIGGER auto-generates user_id (C00001, T00001)
    // and automatically creates a blank row in the respective profile table.
    const [userResult] = await connection.execute(
      `INSERT INTO users (role, phone, email, password_hash, dob) 
       VALUES (?, ?, ?, ?, ?)`,
      [data.role, data.phone, data.email, passwordHash, data.dob || null],
    );

    // Get the auto-generated user_id from LAST_INSERT_ID()
    const [idResult] = await connection.execute(
      "SELECT LAST_INSERT_ID() as lastId",
    );
    const numericId = idResult[0].lastId;

    // Get the actual VARCHAR user_id generated by trigger
    const [userRows] = await connection.execute(
      "SELECT user_id FROM users WHERE role = ? AND phone = ? ORDER BY created_at DESC LIMIT 1",
      [data.role, data.phone],
    );

    const userId = userRows[0]?.user_id;

    if (!userId) {
      throw new Error("Failed to generate user_id - check database triggers");
    }

    // 2. UPDATE the auto-generated profile row with the user's Full Name
    if (data.role === "customer") {
      await connection.execute(
        `UPDATE customer_profile SET customer_name = ? WHERE customer_id = ?`,
        [data.fullName, userId],
      );
    } else if (data.role === "tailor") {
      await connection.execute(
        `UPDATE tailor_shop_profile SET tailor_name = ? WHERE tailor_id = ?`,
        [data.fullName, userId],
      );
    }

    console.log(" New user created:", { userId, numericId });

    await connection.commit();

    // Generate JWT with VARCHAR user_id
    const token = jwt.sign(
      { userId, role: data.role },
      process.env.JWT_SECRET,
      { expiresIn: "7d" },
    );

    res.json({
      success: true,
      message: "User created successfully!",
      userId,
      role: data.role,
      token,
    });
  } catch (error) {
    await connection.rollback();
    console.error("Signup error:", error);
    res.status(400).json({
      success: false,
      error: error.message,
    });
  } finally {
    connection.release();
  }
};

